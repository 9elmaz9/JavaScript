var age = 30;
console.log(age); // 30

var name = "John";
console.log(name); // John


var isMarried = false;
console.log(isMarried); // false


//Object datatype voorbeeld:
var person = {
    firstName: "John",
    lastName: "Doe",
    age: 30
};
console.log(person); // {firstName: "John", lastName: "Doe", age: 30}


var cars = ["Saab", "Volvo", "BMW"];
console.log(cars); // ["Saab", "Volvo", "BMW"]

//Function datatype 
var sayHello = function() {
    console.log("Hello");
}
sayHello(); // Hello

//Undefined datatype 
var x;
console.log(x); // undefined


var x = null;
console.log(x); // null



//NaN datatype 
var x = NaN;
console.log(x); // NaN



var x = Infinity;
console.log(x); // Infinity

